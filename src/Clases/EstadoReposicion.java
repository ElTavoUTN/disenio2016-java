package Clases;

public class EstadoReposicion extends Entidad {

    private int codigoEstadoReposicion;
    private String nombreEstadoReposicion;

    public EstadoReposicion() {
    }

    public int getCodigoEstadoReposicion() {
        return codigoEstadoReposicion;
    }

    public void setCodigoEstadoReposicion(int codigoEstadoReposicion) {
        this.codigoEstadoReposicion = codigoEstadoReposicion;
    }

    public String getNombreEstadoReposicion() {
        return nombreEstadoReposicion;
    }

    public void setNombreEstadoReposicion(String nombreEstadoReposicion) {
        this.nombreEstadoReposicion = nombreEstadoReposicion;
    }

}
