    public RankearProveedor4(DTOCabeceraRanking dtocabecera, Date desde, Date hasta){
        initComponents();
        fechadesde = desde; fechahasta = hasta;
        this.setTitle("Guardar Informe en PDF");
        this.setLocationRelativeTo(null);
        PdfPCell puestocell = new PdfPCell(new Phrase("Puesto"));
        PdfPCell proveedorcell= new PdfPCell(new Phrase("Proveedor"));
        PdfPCell codigoproveedorcell= new PdfPCell(new Phrase("Codigo Proveedor"));
        PdfPCell descuentoproveedorcell= new PdfPCell(new Phrase("Descuento"));
        puestocell.setBackgroundColor(BaseColor.LIGHT_GRAY);
        puestocell.setHorizontalAlignment(1);
        proveedorcell.setBackgroundColor(BaseColor.LIGHT_GRAY);
        proveedorcell.setHorizontalAlignment(1);
        codigoproveedorcell.setBackgroundColor(BaseColor.LIGHT_GRAY);
        codigoproveedorcell.setHorizontalAlignment(1);
        descuentoproveedorcell.setBackgroundColor(BaseColor.LIGHT_GRAY);
        descuentoproveedorcell.setHorizontalAlignment(1);
        tablapdf.addCell(puestocell);
        tablapdf.addCell(proveedorcell);
        tablapdf.addCell(codigoproveedorcell);
        tablapdf.addCell(descuentoproveedorcell);
        Integer i=1;
        for(DTORankingProveedor dtolist : dtocabecera.getM_DTORankingProveedorlist()){
            PdfPCell puesto = new PdfPCell(new Phrase(i.toString())); i++;
            puesto.setHorizontalAlignment(1);
            PdfPCell proveedor = new PdfPCell(new Phrase(dtolist.getNombreProveedor()));
            proveedor.setHorizontalAlignment(1);
            PdfPCell codigoproveedor = new PdfPCell(new Phrase(dtolist.getCodigoProveedor()));
            codigoproveedor.setHorizontalAlignment(1);
            PdfPCell descuentoproveedor = new PdfPCell(new Phrase(decimal.format(dtolist.getTotalDescuento())+"%"));
            descuentoproveedor.setHorizontalAlignment(1);
            tablapdf.addCell(puesto);
            tablapdf.addCell(proveedor);
            tablapdf.addCell(codigoproveedor);
            tablapdf.addCell(descuentoproveedor);
        }
        
        
    }
