    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        String direccion = jTextFieldDireccion.getText();
        System.out.println("Valor direccion "+direccion);

try{
	if(direcionbandera) throw new ManejoExcepciones("Falta Direccion Archivo");
        FileOutputStream archivo = new FileOutputStream(direccion+".pdf");
	Document doc = new Document();
	PdfWriter.getInstance(doc, archivo);
	doc.open();
        doc.add(new Phrase("Informe Rankear Proveedor"));
        doc.add(new Phrase("     "));
        doc.add(new Phrase("Ranking desde "+String.valueOf(formatofecha.format(fechadesde))+" hasta "+String.valueOf(formatofecha.format(fechahasta))));
        doc.add(new com.itextpdf.text.pdf.PdfPTable(tablapdf));
	doc.add(new Phrase("Fecha de Constulta: "+String.valueOf(formatofecha.format(today)+"             Hora de consulta: "+String.valueOf(formatohora.format(today)))));
        doc.close();
	JOptionPane.showMessageDialog(null,"Documento creado con exito");
        this.setVisible(false);

}catch(ManejoExcepciones me){ if(me.getParametro()=="Falta Direccion Archivo"){
        JOptionPane.showMessageDialog(null, "Ingrese direccion donde desea guardar el achivo");}
}
 
 catch(Exception e){System.out.println("Error "+e);}

        
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed

